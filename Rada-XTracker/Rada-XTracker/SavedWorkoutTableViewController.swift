            // Delete the row from the data source
            let ex = WorkoutController.sharedInstance.fetchExercises[indexPath.row]
            WorkoutController.sharedInstance.removeExerciseInBackground(ex)
            tableView.deleteRowsAtIndexPaths([indexPath], withRowAnimation: .Fade)
        }
    }

